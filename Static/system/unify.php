function unifiedScriptsURL ($scripts)
{
    $mtime = 0;

    foreach ($scripts as $script) {
        if (($tmp = @filemtime($script)) > $mtime) {
            $mtime = $tmp;
        }
    }

    $scripts = urlencode(base64_encode(serialize($scripts)));

    return WEB_ROOT."/modules/Static/scripts.php?d={$scripts}&{$mtime}";
}

